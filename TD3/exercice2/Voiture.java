public class Voiture{
    int numero;
    String marque;
    String modele;
    float prix;
    int nombre_exemplaire;

    public Voiture(int num, String marq, String mod, float pri, int nombre){
        numero = num;
        marque = marq;
        modele = mod;
        prix = pri;
        nombre_exemplaire = nombre;
    }
}

